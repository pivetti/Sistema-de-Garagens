    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    try {
        Garagem g = new Garagem();
        g.setNome(txtNome.getText());
        g.setPais(txtPais.getText());
        g.setEstado(txtEstado.getText());
        g.setCidade(txtCidade.getText());
        g.setBairro(txtBairro.getText());
        g.setRua(txtRua.getText());
        g.setNumero(txtNumero.getText());
        g.setCep(txtCep.getText());

        garagemDAO.salvar(g, idUsuario);

        // Atualiza tabela
        atualizaTabela();

        // Limpa os campos
        txtNome.setText("");
        txtPais.setText("");
        txtEstado.setText("");
        txtCidade.setText("");
        txtBairro.setText("");
        txtRua.setText("");
        txtNumero.setText("");
        txtCep.setText("");

        System.out.println("Garagem salva com sucesso!");
    } catch (SQLException e) {
        System.out.println("Erro ao salvar garagem: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSalvarActionPerformed

