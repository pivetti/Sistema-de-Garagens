private void atualizaTabela() throws SQLException{
    DefaultTableModel modelo = (DefaultTableModel) tblGaragens.getModel();
    modelo.setNumRows(0);
    ArrayList<Garagem> garagens = new ArrayList<>();
    
    try{
        garagens = garagemDAO.retornaListaDeGaragens(idUsuario);
        System.out.println(garagens.toString());
        
        for(Garagem g : garagens) {
        
            Object [] linha = {
                g.getId(),
                g.getNome(),
                g.getPais(),
                g.getEstado(),
                g.getCidade(),
                g.getBairro(),
                g.getRua(),
                g.getNumero(),
                g.getCep()};
            modelo.addRow(linha);
        }
    }catch (SQLException e) {
        System.out.println("ERRO: ao carregar a tabela na view -> " + e);
    }
}

